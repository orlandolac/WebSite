final class Default_Model_Diretorio {

    public static function getDiretorioPessoa($pessoa_id) {
        preg_match_all('/[0-9]{2}+/', str_pad($pessoa_id, 10, 0, STR_PAD_LEFT), $pessoa_id);
        return '/uploads/pessoas/' . implode('/', $pessoa_id[0]);
    }

}